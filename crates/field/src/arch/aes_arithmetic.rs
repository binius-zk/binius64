//! AES tower-field byte arithmetic, keyed on the underlier.

use std::{
	array,
	iter::Sum,
	ops::{Add, AddAssign, Mul, Sub, SubAssign},
};

use crate::{
	aes_field::{AESTowerField8b, aes_mul_8b},
	arch::{PackedPrimitiveType, strategies::LaneWideProduct},
	arithmetic_traits::{InvertOrZero, Square, WideMul},
	underlier::{Divisible, ScaledUnderlier, UnderlierType},
};

/// AES tower-field byte arithmetic for a packing whose underlier is `Self`.
///
/// Methods act on the raw underlier, applying the operation to each GF(2^8) byte lane.
pub trait PackedAesArithmetic: UnderlierType {
	/// Unreduced widening-multiply product.
	///
	/// Accumulates by XOR, then folds back once via [`reduce`](Self::reduce).
	/// Only the NEON `vmull` path defers a real reduction.
	/// GFNI and the byte tables already reduce each product.
	/// For them this is the reduced packing and `reduce` is a no-op.
	type WideProduct: Copy
		+ Default
		+ Sum
		+ Add<Output = Self::WideProduct>
		+ AddAssign
		+ Sub<Output = Self::WideProduct>
		+ SubAssign;

	/// Widening multiply of the packed bytes, deferring reduction.
	fn wide_mul(a: Self, b: Self) -> Self::WideProduct;

	/// Fold an accumulated wide product back to packed bytes.
	fn reduce(wide: Self::WideProduct) -> Self;

	/// Square each packed byte.
	fn square(a: Self) -> Self;

	/// Invert each packed byte, mapping zero to zero.
	fn invert_or_zero(a: Self) -> Self;

	/// Multiply each packed byte as `reduce(wide_mul(a, b))`.
	///
	/// The widening multiply is the single source of truth for both `Mul` and `WideMul`.
	#[inline]
	fn mul(a: Self, b: Self) -> Self {
		Self::reduce(Self::wide_mul(a, b))
	}
}

impl<U: PackedAesArithmetic> Mul for PackedPrimitiveType<U, AESTowerField8b> {
	type Output = Self;

	#[inline]
	fn mul(self, rhs: Self) -> Self {
		Self::from_underlier(U::mul(self.to_underlier(), rhs.to_underlier()))
	}
}

impl<U: PackedAesArithmetic> Square for PackedPrimitiveType<U, AESTowerField8b> {
	#[inline]
	fn square(self) -> Self {
		Self::from_underlier(U::square(self.to_underlier()))
	}
}

impl<U: PackedAesArithmetic> InvertOrZero for PackedPrimitiveType<U, AESTowerField8b> {
	#[inline]
	fn invert_or_zero(self) -> Self {
		Self::from_underlier(U::invert_or_zero(self.to_underlier()))
	}
}

impl<U: PackedAesArithmetic> WideMul for PackedPrimitiveType<U, AESTowerField8b> {
	type Output = U::WideProduct;

	#[inline]
	fn wide_mul(a: Self, b: Self) -> Self::Output {
		U::wide_mul(a.to_underlier(), b.to_underlier())
	}

	#[inline]
	fn reduce(wide: Self::Output) -> Self {
		Self::from_underlier(U::reduce(wide))
	}
}

impl PackedAesArithmetic for u8 {
	type WideProduct = PackedPrimitiveType<u8, AESTowerField8b>;

	#[inline]
	fn wide_mul(a: u8, b: u8) -> Self::WideProduct {
		PackedPrimitiveType::from_underlier(aes_mul_8b(a, b))
	}

	#[inline]
	fn reduce(wide: Self::WideProduct) -> u8 {
		wide.to_underlier()
	}

	#[inline]
	fn square(a: u8) -> u8 {
		AES_TOWER_8B_SQUARE_MAP[a as usize]
	}

	#[inline]
	fn invert_or_zero(a: u8) -> u8 {
		AES_TOWER_8B_INVERT_MAP[a as usize]
	}
}

/// Lane-wise widening multiply of a byte-divisible underlier via the `u8` byte multiply.
#[inline]
pub(crate) fn bytewise_wide_mul<U: UnderlierType + Divisible<u8>>(
	a: U,
	b: U,
) -> PackedPrimitiveType<U, AESTowerField8b> {
	let bytes = Divisible::<u8>::value_iter(a)
		.zip(Divisible::<u8>::value_iter(b))
		.map(|(x, y)| <u8 as PackedAesArithmetic>::mul(x, y));
	PackedPrimitiveType::from_underlier(Divisible::<u8>::from_iter(bytes))
}

/// Lane-wise square of a byte-divisible underlier via the `u8` byte square table.
#[inline]
pub(crate) fn bytewise_square<U: Divisible<u8>>(a: U) -> U {
	Divisible::<u8>::from_iter(
		Divisible::<u8>::value_iter(a).map(<u8 as PackedAesArithmetic>::square),
	)
}

/// Lane-wise invert-or-zero of a byte-divisible underlier via the `u8` byte invert table.
#[inline]
pub(crate) fn bytewise_invert_or_zero<U: Divisible<u8>>(a: U) -> U {
	Divisible::<u8>::from_iter(
		Divisible::<u8>::value_iter(a).map(<u8 as PackedAesArithmetic>::invert_or_zero),
	)
}

impl<U: PackedAesArithmetic, const N: usize> PackedAesArithmetic for ScaledUnderlier<U, N>
where
	ScaledUnderlier<U, N>: UnderlierType,
{
	type WideProduct = LaneWideProduct<U::WideProduct, N>;

	#[inline]
	fn wide_mul(a: Self, b: Self) -> Self::WideProduct {
		LaneWideProduct(array::from_fn(|i| U::wide_mul(a.0[i], b.0[i])))
	}

	#[inline]
	fn reduce(wide: Self::WideProduct) -> Self {
		ScaledUnderlier(array::from_fn(|i| U::reduce(wide.0[i])))
	}

	#[inline]
	fn square(a: Self) -> Self {
		ScaledUnderlier(a.0.map(U::square))
	}

	#[inline]
	fn invert_or_zero(a: Self) -> Self {
		ScaledUnderlier(a.0.map(U::invert_or_zero))
	}
}

